#!/usr/bin/env node
/**
 * AP-091: API operation inventory + test-matrix CI gate.
 *
 * Ensures:
 *  1. docs/openapi.yaml operation set == runtime Gin routes (all paths, not only /api/v1)
 *  2. docs/api-operations.inventory.json is generated from OpenAPI and committed fresh
 *  3. docs/api-test-matrix.json covers every operationId with ≥1 success and ≥1 failure test ref
 *  4. backend contract matrix test references every operationId
 *
 * Usage:
 *   node scripts/api-test-matrix-gate.mjs           # verify
 *   node scripts/api-test-matrix-gate.mjs --write   # regenerate inventory + matrix stubs + markdown
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const ROUTER = path.join(ROOT, "backend/internal/routes/router.go");
const OPENAPI = path.join(ROOT, "docs/openapi.yaml");
const INVENTORY = path.join(ROOT, "docs/api-operations.inventory.json");
const MATRIX_JSON = path.join(ROOT, "docs/api-test-matrix.json");
const MATRIX_MD = path.join(ROOT, "docs/api-test-matrix.md");
const CONTRACT_TEST = path.join(
  ROOT,
  "backend/internal/routes/contract_matrix_test.go",
);

const HTTP_METHODS = new Set(["GET", "POST", "PUT", "PATCH", "DELETE", "OPTIONS", "HEAD"]);
const WRITE = process.argv.includes("--write");

function die(msg) {
  console.error(`api-test-matrix-gate: ${msg}`);
  process.exit(1);
}

function read(file) {
  if (!fs.existsSync(file)) die(`missing file: ${path.relative(ROOT, file)}`);
  return fs.readFileSync(file, "utf8");
}

function normalizePath(p) {
  let s = String(p || "").trim();
  s = s.replace(/:([A-Za-z_][A-Za-z0-9_]*)/g, "{$1}");
  s = s.replace(/\{([^}]+)\}/g, (_, name) => `{${String(name).split("=")[0]}}`);
  s = s.replace(/\/{2,}/g, "/");
  if (s.length > 1 && s.endsWith("/")) s = s.slice(0, -1);
  return s;
}

function key(method, p) {
  return `${String(method).toUpperCase()} ${normalizePath(p)}`;
}

/** Static parse of router.go Gin registrations (shared approach with AP-033). */
function extractGinRoutes(src) {
  const routes = new Map();
  const groups = new Map([["r", ""]]);
  const groupRe =
    /\b([A-Za-z_]\w*)\s*:?=\s*([A-Za-z_]\w*)\.Group\(\s*"([^"]*)"\s*\)/g;
  let m;
  while ((m = groupRe.exec(src))) {
    const [, varName, parent, prefix] = m;
    const parentPrefix = groups.has(parent) ? groups.get(parent) : "";
    groups.set(varName, `${parentPrefix}${prefix}`);
  }
  const routeRe =
    /\b([A-Za-z_]\w*)\.(GET|POST|PUT|PATCH|DELETE|OPTIONS|HEAD)\(\s*"([^"]*)"/g;
  while ((m = routeRe.exec(src))) {
    const [, recv, method, p] = m;
    const prefix = groups.has(recv) ? groups.get(recv) : "";
    const full = normalizePath(`${prefix}${p}`);
    const k = key(method, full);
    const line = src.slice(0, m.index).split("\n").length;
    if (!routes.has(k)) {
      routes.set(k, { method: method.toUpperCase(), path: full, line, source: "gin" });
    }
  }
  return routes;
}

/** Extract OpenAPI operations: method, path, operationId. */
function extractOpenAPIOperations(yamlText) {
  const ops = [];
  let currentPath = null;
  let currentMethod = null;
  let inPaths = false;
  const lines = yamlText.split(/\r?\n/);
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (/^paths:\s*$/.test(line)) {
      inPaths = true;
      continue;
    }
    if (inPaths && /^[A-Za-z]/.test(line) && !/^\s/.test(line)) {
      // left paths section
      break;
    }
    if (!inPaths) continue;

    const pathMatch = line.match(/^  (\/[^:]*):\s*$/);
    if (pathMatch) {
      currentPath = pathMatch[1];
      currentMethod = null;
      continue;
    }
    const methodMatch = line.match(/^    (get|post|put|patch|delete|options|head):\s*$/i);
    if (methodMatch && currentPath) {
      currentMethod = methodMatch[1].toUpperCase();
      continue;
    }
    const opMatch = line.match(/^      operationId:\s*(\S+)\s*$/);
    if (opMatch && currentPath && currentMethod) {
      ops.push({
        method: currentMethod,
        path: normalizePath(currentPath),
        operationId: opMatch[1],
        line: i + 1,
      });
      currentMethod = null;
    }
  }
  return ops;
}

function defaultMatrixEntry(op) {
  return {
    id: `API-${op.operationId}`,
    operationId: op.operationId,
    method: op.method,
    path: op.path,
    success_tests: [`TestContractMatrix/${op.operationId}/success`],
    failure_tests: [`TestContractMatrix/${op.operationId}/failure`],
    notes: "Generated by scripts/api-test-matrix-gate.mjs; contract smoke in contract_matrix_test.go",
  };
}

function buildMarkdown(inventory, matrix) {
  const lines = [];
  lines.push("# Animal Poke · API 测试矩阵");
  lines.push("");
  lines.push(
    `> 覆盖范围：OpenAPI \`${inventory.operations.length}\` 个 operationId（与 Gin runtime 双向对齐）。`,
  );
  lines.push(
    `> 本文件由 \`node scripts/api-test-matrix-gate.mjs --write\` 从 inventory/matrix 生成；禁止手工改计数。`,
  );
  lines.push("");
  lines.push("## 门禁");
  lines.push("");
  lines.push("- `node scripts/openapi-contract-gate.mjs` — `/api/v1` Gin ↔ OpenAPI（AP-033）");
  lines.push(
    "- `node scripts/api-test-matrix-gate.mjs` — 全量 operation inventory + 矩阵 + 契约测试引用（AP-091）",
  );
  lines.push(
    "- `go test ./internal/routes -run TestContractMatrix` — 每个 operation 至少一个 success + failure",
  );
  lines.push("");
  lines.push("## Endpoint 矩阵");
  lines.push("");
  lines.push("| ID | Method | Path | operationId | Success tests | Failure tests |");
  lines.push("|---|---|---|---|---|---|");
  for (const row of matrix.operations) {
    lines.push(
      `| ${row.id} | \`${row.method}\` | \`${row.path}\` | \`${row.operationId}\` | ${row.success_tests.join(", ")} | ${row.failure_tests.join(", ")} |`,
    );
  }
  lines.push("");
  lines.push("## 全局断言");
  lines.push("");
  lines.push("- 成功/失败均回传 `X-Request-ID`（JSON API）。");
  lines.push("- 错误体包含稳定 `reason_code`（若适用），不泄露 Secret/DSN/Provider 原文。");
  lines.push("- 401/403 语义稳定；缺依赖时 503 而非 404。");
  lines.push("- 矩阵与 inventory 数量必须一致；CI 禁止 skip 掩盖缺口。");
  lines.push("");
  return lines.join("\n");
}

function main() {
  const gin = extractGinRoutes(read(ROUTER));
  const oasOps = extractOpenAPIOperations(read(OPENAPI));
  if (oasOps.length === 0) die("no OpenAPI operations parsed");

  // Bidirectional: every OpenAPI op must exist in Gin; every Gin route should be in OpenAPI.
  const oasMap = new Map();
  for (const op of oasOps) {
    const k = key(op.method, op.path);
    if (oasMap.has(k)) die(`duplicate OpenAPI route ${k}`);
    oasMap.set(k, op);
  }

  const onlyGin = [];
  const onlyOas = [];
  for (const [k, r] of gin) {
    if (!HTTP_METHODS.has(r.method)) continue;
    // metrics may be registered as 404 placeholder on public router — still a route
    if (!oasMap.has(k)) onlyGin.push(r);
  }
  for (const [k, op] of oasMap) {
    if (!gin.has(k)) onlyOas.push(op);
  }

  const inventory = {
    generated_by: "scripts/api-test-matrix-gate.mjs",
    openapi: "docs/openapi.yaml",
    router: "backend/internal/routes/router.go",
    count: oasOps.length,
    operations: oasOps
      .map((op) => ({
        method: op.method,
        path: op.path,
        operationId: op.operationId,
        key: key(op.method, op.path),
      }))
      .sort((a, b) => a.key.localeCompare(b.key)),
  };

  let matrix;
  if (WRITE || !fs.existsSync(MATRIX_JSON)) {
    matrix = {
      generated_by: "scripts/api-test-matrix-gate.mjs",
      count: inventory.count,
      operations: inventory.operations.map((op) =>
        defaultMatrixEntry({
          method: op.method,
          path: op.path,
          operationId: op.operationId,
        }),
      ),
    };
  } else {
    matrix = JSON.parse(read(MATRIX_JSON));
  }

  if (WRITE) {
    fs.writeFileSync(INVENTORY, JSON.stringify(inventory, null, 2) + "\n");
    // merge: keep notes if present
    const prev = fs.existsSync(MATRIX_JSON)
      ? JSON.parse(read(MATRIX_JSON))
      : { operations: [] };
    const prevById = new Map((prev.operations || []).map((r) => [r.operationId, r]));
    matrix = {
      generated_by: "scripts/api-test-matrix-gate.mjs",
      count: inventory.count,
      operations: inventory.operations.map((op) => {
        const base = defaultMatrixEntry(op);
        const old = prevById.get(op.operationId);
        if (!old) return base;
        return {
          ...base,
          success_tests:
            Array.isArray(old.success_tests) && old.success_tests.length
              ? old.success_tests
              : base.success_tests,
          failure_tests:
            Array.isArray(old.failure_tests) && old.failure_tests.length
              ? old.failure_tests
              : base.failure_tests,
          notes: old.notes || base.notes,
        };
      }),
    };
    fs.writeFileSync(MATRIX_JSON, JSON.stringify(matrix, null, 2) + "\n");
    fs.writeFileSync(MATRIX_MD, buildMarkdown(inventory, matrix));
    console.log(
      `Wrote inventory (${inventory.count}), matrix JSON/MD.`,
    );
  }

  // Verify committed inventory
  if (!fs.existsSync(INVENTORY)) {
    die(`missing ${path.relative(ROOT, INVENTORY)}; run with --write`);
  }
  const committedInv = JSON.parse(read(INVENTORY));
  const invCanonical = JSON.stringify(inventory, null, 2) + "\n";
  const committedCanonical = JSON.stringify(committedInv, null, 2) + "\n";
  // compare by content of operations
  if (committedInv.count !== inventory.count) {
    die(
      `inventory count ${committedInv.count} != openapi ${inventory.count}; run with --write`,
    );
  }
  const committedIds = new Set(
    (committedInv.operations || []).map((o) => o.operationId),
  );
  const liveIds = new Set(inventory.operations.map((o) => o.operationId));
  for (const id of liveIds) {
    if (!committedIds.has(id)) die(`inventory missing operationId ${id}; run --write`);
  }
  for (const id of committedIds) {
    if (!liveIds.has(id)) die(`inventory has stale operationId ${id}; run --write`);
  }

  // Matrix coverage
  if (!fs.existsSync(MATRIX_JSON)) die(`missing matrix JSON; run --write`);
  matrix = JSON.parse(read(MATRIX_JSON));
  if (matrix.count !== inventory.count) {
    die(`matrix count ${matrix.count} != inventory ${inventory.count}; run --write`);
  }
  const matrixById = new Map((matrix.operations || []).map((r) => [r.operationId, r]));
  for (const op of inventory.operations) {
    const row = matrixById.get(op.operationId);
    if (!row) die(`matrix missing ${op.operationId}`);
    if (!Array.isArray(row.success_tests) || row.success_tests.length === 0) {
      die(`matrix ${op.operationId} missing success_tests`);
    }
    if (!Array.isArray(row.failure_tests) || row.failure_tests.length === 0) {
      die(`matrix ${op.operationId} missing failure_tests`);
    }
  }
  for (const id of matrixById.keys()) {
    if (!liveIds.has(id)) die(`matrix has stale operationId ${id}`);
  }

  // Contract test must load inventory-driven matrix (AP-091 dynamic coverage).
  if (!fs.existsSync(CONTRACT_TEST)) {
    die(`missing ${path.relative(ROOT, CONTRACT_TEST)}`);
  }
  const contractSrc = read(CONTRACT_TEST);
  if (!contractSrc.includes("TestContractMatrix")) {
    die("contract_matrix_test.go missing TestContractMatrix");
  }
  if (
    !contractSrc.includes("api-operations.inventory.json") &&
    !contractSrc.includes("loadInventoryOps")
  ) {
    die(
      "contract_matrix_test.go must load docs/api-operations.inventory.json (loadInventoryOps)",
    );
  }
  // Each matrix row points at TestContractMatrix/<op>/{success,failure}
  for (const op of inventory.operations) {
    const row = matrixById.get(op.operationId);
    const needSuccess = `TestContractMatrix/${op.operationId}/success`;
    const needFailure = `TestContractMatrix/${op.operationId}/failure`;
    if (!row.success_tests.includes(needSuccess)) {
      die(`matrix ${op.operationId} success_tests must include ${needSuccess}`);
    }
    if (!row.failure_tests.includes(needFailure)) {
      die(`matrix ${op.operationId} failure_tests must include ${needFailure}`);
    }
  }

  // Gin ↔ OpenAPI full path parity
  if (onlyGin.length || onlyOas.length) {
    if (onlyGin.length) {
      console.error(`\n  Runtime routes missing from OpenAPI (${onlyGin.length}):`);
      for (const r of onlyGin) {
        console.error(`    - ${r.method} ${r.path}  (router.go:${r.line})`);
      }
    }
    if (onlyOas.length) {
      console.error(`\n  OpenAPI paths missing from runtime (${onlyOas.length}):`);
      for (const r of onlyOas) {
        console.error(`    - ${r.method} ${r.path}  (operationId=${r.operationId})`);
      }
    }
    die("Gin ↔ OpenAPI full inventory mismatch");
  }

  console.log(`AP-091 API test matrix gate`);
  console.log(`  OpenAPI operations: ${inventory.count}`);
  console.log(`  Gin routes:         ${gin.size}`);
  console.log(`  Matrix rows:        ${matrix.operations.length}`);
  console.log(`  Contract test:      ${path.relative(ROOT, CONTRACT_TEST)}`);
  console.log(`  OK: inventory, matrix, and contract references aligned`);
  for (const op of inventory.operations) {
    console.log(`    ✓ ${op.method} ${op.path}  (${op.operationId})`);
  }
  // silence unused
  void invCanonical;
  void committedCanonical;
  void createRequire;
}

main();
